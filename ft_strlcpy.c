#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int str_len;
	unsigned int i;

	if (!dst && !src)
		return (0);
	str_len = ft_strlen(src);
	if (size == 0)
		return (str_len);
	i = 0;
	while (src[i] != '\0' && i < (size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (str_len);
}
